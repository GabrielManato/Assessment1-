#08-SimpleSearch

# List of names
names = ["Jake", "Zac", "Ian", "Ron", "Sam", "Dave"]

# The string to search for
search_term = "Sam"

# Search for the name in the list
if search_term in names:
    print(f"{search_term} is in the list.")
else:
    print(f"{search_term} is not in the list.")
